package Praktikum.order;

public class Ingridient {
    private String _id;
    private String name;

    public Ingridient(String _id, String name) {
        this._id = _id;
        this.name = name;
    }

    public String getid() {
        return _id;
    }

    public String getName() {
        return name;
    }

}
